        <h1>Lizenzen</h1>
        <img border="0" src="../images/neu/lizenz.jpg" width="142" height="103" align="right" alt="Lizenz"/>
        <p>
            Zur besseren Zuordnung von Spielern zu ihren Mannschaften werden wir ab
            2009 neben dem Verein, Name und Lizenznummer auch die Zuordnung zur
            jeweiligen Mannschaft und den jeweilige Mannschaftsführer veröffentlichen.
        </p>
        <p>
            Ab 2009 entspricht die Spielernummer auf dem Mannschaftsmeldebogen
            der Lizenznummer de(r)s jeweiligen SpielerIn.
        </p>
        <p>Beispiel: Lizenznummer <strong>01-156-007</strong> --> Spielernummer <strong>7</strong></p>
