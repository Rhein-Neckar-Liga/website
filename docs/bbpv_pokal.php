        <h1>BBPV Pokal 2010</h1>
        <h2>Modus</h2>
        <p>Seit der Saison 2007 findet diese regionsübergreifende Pokalrunde unter Leitung
           des BBPV statt. Über das Abschneiden der teilnehmenden Vereine unserer Region werden
           wir hier auf dieser Seite zeitnah berichten.</p>
        <h3>Folgendes ist zu beachten:</h3>
        <ul>
            <li>Pro Verein eine 6er-Mannschaft (Einstufung gemäß höchst
                spielender Mannschaft im Ligabetrieb 2010)</li>
            <li>2 zusätzliche Ersatzspieler pro Pokalbegegnung möglich</li>
            <li>Meldung bis zum <strong>28.02.2010</strong></li>
        </ul>
        <h3>Modus</h3>
        <p>sog. Europapokal-Modus mit 3 Runden</p>
        <ul>
            <li>1. Runde: 3:3 vs. 3:3 und 3:3m vs. 3:3m</li>
            <li>2. Runde: 2:2 vs. 2:2, 2:2 vs. 2:2, 2:2m vs. 2:2m</li>
            <li>3. Runde: 3:3 vs. 3:3m und 3:3m vs. 3:3</li>
        </ul>
        <h2>Termine</h2>
        <p>Die 3. Runde im BBPV-Pokal ist ausgelost und sollte bis zum 31.10.2010 gespielt sein</p>
        <p>Mehr Informationen über den Cup sowie die Ausschreibung und das
            Anmeldeformular findet ihr auf <a href="http://www.petanque-bw.de/index.php?id=382">den Seiten des BBPV</a></p>
