        <h1>Sportordnung für den Spielbetrieb</h1>
        <p>In der Fassung vom  26.01.2011</p>
        <p>Deutscher Pétanque-Verband - <br />
            Landesverband Baden-Württemberg - <br />
            Region Rhein-Neckar</p>
        <ol>
            <li>
                <p>Grundlage sind die Bestimmungen der Sportordnung und
                    der Richtlinie zur Durchführung des Ligaspielbetriebes des
                    Landesverbandes Baden-Württemberg.</p>
            </li>
            <li>
                <p>Die Ligastruktur ist 4-stufig aufgebaut. Als
		höchste Staffel gilt die Oberliga, gefolgt von der Landesliga,
		Bezirksliga und der Kreisliga. Bei Bedarf kann, immer bei der untersten
		Staffel beginnend, eine horizontale Teilung in Staffel A und B
		vorgenommen werden. Für alle Ligastaffeln gelten als Obergrenze 12
		Mannschaften und nach Möglichkeit sollten nicht weniger als 8
		Mannschaften pro Ligastaffel vertreten sein. Die Einführung der
		Mixteregelung, ab Ober/Landesliga abwärts, wird durch die
		Vereinsvertreter der jeweils betroffenen Ligastaffeln auf der
		Mitgliederversammlung mit einfacher Mehrheit festgelegt. Bei
		Stimmengleichheit entscheidet die Ligaleitung.</p>
                <p>Im Regionalverband Rhein-Neckar gilt ab 2011 nachfolgende Ligastruktur:</p>
                <ul>
                    <li>Oberliga 12 Mannschaften ohne Mixteregelung</li>
                    <li>Landesliga 12 Mannschaften ohne Mixteregelung</li>
                    <li>Bezirksliga 11 Mannschaften ohne Mixteregelung</li>
                    <li>Kreisliga 8 Mannschaften ohne Mixteregelung</li>
                </ul>
            </li>
            <li>
                <p>Die genaue Anzahl der Absteiger ist abhängig vom
		Ausgang der Bundesliga, der Baden-Württemberg-Liga sowie der
		Regionalliga Nord und kann sich deshalb bis zur Kreisliga durchschlagen.
		Unabhängig davon steigt jeder Meister seiner Ligastaffel in die nächst
		höhere Ligastaffel auf. Ab Landesliga abwärts steigt zusätzlich der
		Vizemeister in die nächst höhere Ligastaffel auf.</p>
                <ul>
                    <li>Meister der Oberliga in die Regionalliga Nord</li>
                    <li>Meister und Vizemeister der Landesliga in die Oberliga</li>
                    <li>Meister und Vizemeister der Bezirksliga in die Landesliga</li>
                    <li>Meister und Vizemeister der Kreisliga in die Bezirksliga</li>
                </ul>
                <p>Tritt eine Mannschaft zur neuen Spielrunde nicht mehr an, so nimmt deren
		Platz die bestplatzierte niederrangige Mannschaft der zurückliegenden
		Saison ein. Am Ende der Ligarunde steigt mindestens der Letzte seiner
		jeweiligen Ligastaffel, Ausnahme Kreisliga, in die darunter angesiedelte
		Ligastaffel ab.</p>
                <ul>
                    <li>Letzter und Vorletzter der Oberliga in die Landesliga</li>
                    <li>Letzter und Vorletzter der Landesliga in die Bezirksliga</li>
                    <li>Letzter und Vorletzter der Bezirksliga in die Kreisliga</li>
                    <li>Letzter der Kreisliga verbleibt in der Kreisliga</li>
                </ul>
            </li>
            <li>
                <p>Mannschaftsmeldungen müssen bis zum 10.01. des
		folgenden Jahres, die Lizenzmeldungen müssen bis zum 31.01. des
		folgenden Jahres und die Spielermeldebögen müssen bis zum 28.02. des
		folgenden Jahres der Ligaleitung gemeldet werden.</p>
                <p>Spieler, die nach diesem Zeitpunkt eine Lizenz erstmals erwerben, sind
		ab dem Zeitpunkt spielberechtigt, ab dem die Lizenz von der
		Geschäftstelle des Landesverbandes ausgestellt wurde. Die Ligaleitung
		ist umgehend über die Neulizenz zu informieren und gibt diese
		schnellstmöglich den teilnehmenden Vereinen bekannt. </p>
                <p>Jeder Verein zahlt vor Beginn der Ligarunde eine Startgebühr in Höhe von
		25,00 € pro gemeldeter Mannschaft.</p>
            </li>
            <li>
                <p>Ein Verein kann mehrere Mannschaften melden. Sie
		werden in numerischer Reihenfolge bezeichnet. Die Mannschaft mit der
		niedrigeren Nummer gilt als die Spielstärkere. Mit dem ersten Einsatz in
		der Liga erfolgt die Zuordnung des Spielers zu einer bestimmten
		Mannschaft. Die Wechselmodalitäten hinsichtlich des Einsatzes von
		Spielern in mehreren Mannschaften richten sich nach der Richtlinie zur
		Durchführung des Ligaspielbetriebes des Landesverbandes
		Baden-Württemberg.<br>
            </li>
            <li>
                <p>Wird ein Spieler regelwidrig eingesetzt, so gilt:</p>
                <ul>
                    <li>die Begegnung wird mit 0:1 / 0:5 / 0:65 gewertet</li>
                    <li>der Verein zahlt 50 € Strafgeld</li>
                </ul>
                <p>Eine Mannschaft, die zu einer Begegnung nicht antritt, zahlt 50,- €
		Strafgeld.</p>
            </li>
            <li>
                <p>Alle Ligen spielen an Großspieltagen. Die Termine
		der Großspieltage werden vom BBPV-Vorstand in Absprache mit den
		Ligaleitern jährlich festgelegt und rechtzeitig bekannt gegeben. Ober-,
		Landes-, Bezirks- und Kreisligen dürfen Abweichungen von dieser Regelung
		beschließen, wenn eine Mehrheit der Mitgliedsvereine in der Ligaregion
		sich dafür ausspricht.</p>
            </li>
            <li>
                <p>Jede Mannschaft spielt im Verlaufe einer Ligarunde
		einmal gegen jede andere Mannschaft der jeweiligen Gruppe.</p>
            </li>
            <li>
                <p>Der ausrichtende Verein hat für ausreichende
		Spielbahnen, für die notwendigen Spielbahnbegrenzungen und für die
		Nummerierung der Spielbahnen zu sorgen. Die Spielbahnennummerzuordnung
		zu den einzelnen Staffeln werden vor Ligabeginn von der Ligaleitung
		festgelegt und auf der RNL Webseite veröffentlicht.</p>
            </li>
            <li>
                <p>In den Spielberichtsbögen sind die Nummern der Bahn
		angegeben, auf der die Partien stattzufinden haben.</p>
            </li>
            <li>
                <p>An jedem Großspieltag sind vor Beginn der ersten
		Begegnung alle Lizenzen einer Mannschaft durch die jeweiligen
		Mannschaftsführer in einem Umschlag dem anwesenden Schiedsrichter bzw.
		dem Staffelleiter zu übergeben. Nach Beendigung aller Spiele sind diese
		wieder von den Mannschaftsführern abzuholen.</p>
                <p>Eine Mannschaft besteht aus mindestens sechs Spielern.</p>
                <p>In den einzelnen Ligen umfasst eine Begegnung zwei Spielrunden. Es
		werden pro Begegnung fünf Pétanquespiele gewertet, die in zwei
		aufeinander folgenden Spielrunden durchgeführt werden müssen.</p>
                <p>In der ersten Spielrunde wird zeitgleich in den Formationen Triplette 1
		und Triplette 2 bzw. mixte gespielt. Die jeweiligen Formationen spielen
		gegeneinander.</p>
                <p>In der zweiten Spielrunde werden zeitgleich in den Formationen Doublette
		1, Doublette 2 und Doublette 3 bzw. mixte gespielt. Die jeweiligen
		Formationen spielen gegeneinander.</p>
                <p>Tritt eine Mannschaft nicht vollzählig an, kann sie spielen, ohne auf
		abwesende Spieler zu warten. Verspätetes Antreten zu einem Spiel wird
		nach dem Reglement der F.I.P.J.P. mit Strafpunkten belegt.</p>
                <p>Wird die Partie letztendlich nicht ausgetragen, wird diese mit 13:0 für
		die antrittsbereite Mannschaft gewertet.</p>
            </li>
            <li>
                <p>Die 1. Begegnung eines Spieltages beginnt in der
		Regel um 09.00 Uhr. Abweichungen davon sind auf der RNL Webseite
		festgelegt und abrufbar.</p>
                <p>Über die Bespielbarkeit eines Platzes (Unwetter, Dunkelheit) entscheiden
		die anwesenden Mannschaftsführer und der Staffelleiter mit einfacher
		Mehrheit. Bei Stimmengleichheit entscheidet die Stimme des
		Staffelleiters.</p>
                <p>Nachholspiele müssen innerhalb der folgenden 14 Tage oder an einem
		freien Ligaspieltag ausgetragen werden.</p>
                <p>Die Spielberichtsbögen sind vom jeweiligen Staffelleiter mitzubringen.</p>
                <p>Vor jeder Begegnung sind alle relevanten Spieldaten vom jeweiligen
		Mannschaftsführer einzutragen.</p>
                <p>Die erstgenannte Mannschaft auf dem Spielberichtsbogen trägt als erstes
		ihre Spieler in den Spielberichtsbogen ein.</p>
                <p>Das Recht die Zielkugel zum ersten Spieldurchgang zu werfen wird durch
		Los ermittelt.</p>
                <p>Nach Ende der Begegnung tragen die Mannschaftsführer die einzelnen
		Spielergebnisse in die Spielberichtsbögen ein und bestätigen die
		Richtigkeit durch ihre Unterschrift.</p>
                <p>Die weiteren Begegnungen eines Spieltages beginnen spätestens zu dem
		Zeitpunkt, an dem die vorhergehenden Begegnungen der zwei betreffenden
		Mannschaften komplett abgeschlossen sind.</p>
                <p>Nach Beendigung aller Begegnungen überprüfen die jeweiligen
		Staffelleiter die Spielergebnisse und bestätigen diese ebenfalls durch
		ihre Unterschrift auf den Spielberichtsbogen. Danach werden die
		Spielberichtsbögen von dem jeweiligen Staffelleiter eingesammelt und
		verwaltet.</p>
                <p>Alle Ergebnisse müssen bis spätestens zum darauf folgenden Montag der
		Ligaleitung übermittelt werden.</p>
            </li>
            <li>
                <p>Die Ligaversammlung wählt den oder die Ligaleiter,
		den oder die Jugendbeauftragten und für jede Staffel einen
		Staffelleiter. Die Ligaleiter und der Jugendbeauftragte werden für die
		Dauer von zwei Jahren gewählt, die Staffelleiter für die Dauer von einem
		Jahr.</p>
            </li>
            <li>
                <p>Neben dem Ligaspielbetrieb findet einmal im Jahr an
		einem Wochenende der Rhein-Neckar-Liga-Pokal statt. Spielberechtigt sind
		Vereine, die dem Regionalverband Rhein-Neckar angehören. </p>
                <p>Gespielt wird nach Modus „6-tête“ über 6 Runden, wobei jede Runde auf
		max. 1 Stunde begrenzt ist.</p>
                <p>Tritt ein Verein, der seine Teilnahme am Rhein-Neckar-Pokal verbindlich
		zugesagt hat, nicht an und hat er seine Nichtteilnahme nicht mindesten 14 Tage vor dem
		Ausrichtungstag des Rhein-Neckar-Pokal der Ligaleitung schriftlich
		angezeigt, zahlt er ein Strafgeld in Höhe von 50,- €.</p>
            </li>
            <li>
                <p>Diese Sportordnung für den Spielbetrieb in den
		Rhein-Neckar-Ligen ist gültig bis zur ersten Ligaversammlung der
		Rhein-Neckar-Liga 2012. Im Rahmen dieser Ligaversammlung wird die
		Sportordnung neu verabschiedet.</p>
            </li>
        </ol>
