        <h1>Rhein-Neckar-Ligapokal 2011</h1>
			<h3>BF Malsch holen den Rhein-Neckar Ligapokal!</h3>
<p class='wichtig'>Bilder jetzt im <a href='?site=bilder&amp;year=2011&amp;album=RNL-Pokal'>&raquo; Bilderalbum</a></p>
<p>
Zum vierten Mal war der RNL-Pokal bei den Schwetzinger F&uuml;chsen in der Ketscher Landstrasse zu Gast und Wetter, Bewirtung und Stimmung waren wieder mal hervorragend.<br/>
Gespielt wurde im Modus 6-tête &uuml;ber 6 Runden mit Zeitbegrenzung von einer Stunde je Partie, was wieder nur die allerwenigsten Begegnungen betraf.<br/>
W&auml;hrend des ganzen Turniers wechselten die Boulefreunde aus Malsch sich mit den Primeurs aus Walldorf mit der Wertungsf&uuml;hrung ab w&auml;hrend am unteren Ende die Bouletten mit den Schwetzinger F&uuml;chsen um die rote Laterne k&auml;mpften.<br/>
Ungebetener und zahlreicher Gast waren die Wespen, am Ende des Turniers z&auml;hlten wir neun Gestochene und in der K&uuml;che waren schon die Zwiebeln ausgegangen - zum Gl&uuml;ck war keine/r der Leidtragenden allergisch. <br/>
Laura Seeger nahm wie schon die vergangenen Jahre kompetent und freundlich die Resultate entgegen und f&uuml;hrte die Ergebnisliste. <br/>
Gegen 17:45 konnte bereits mit der Siegerehrung begonnen werden und jede teilnehmende Mannschaft nahm ein kleines Pr&auml;sent entgegen. <br/> Die Ergebnisse im Detail: 
