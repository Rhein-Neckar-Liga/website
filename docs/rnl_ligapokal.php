        <h1>Rhein-Neckar-Ligapokal 2010</h1>
        <h2>BC Schwetzingen holt den Rhein-Neckar-Liga Pokal!</h2>
        <p class='wichtig'>weitere Bilder jetzt im <a target='_self' href='?site=bilder&amp;year=2010&amp;album=Rhein-Neckar-Liga_Pokal'>&raquo; Bilderalbum</a></p>
        <p>
            Bereits zum dritten Mal durften wir bei schönen Wetter den Rhein-Neckar Ligapokal in Schwetzingen austragen.<br/>
            Die Schwetzinger Füchse waren wieder ein hervorragender Gastgeber, sogar die morgendliche Bewässerung kam pünktlich wie bestellt ;-)<br/>
            Gespielt wurde wieder im Modus 6-tête über 6 Runden mit Zeitbegrenzung von einer Stunde je Partie, was aber nur die allerwenigsten Begegnungen betraf.<br/>
            Die erste Mannschaft des BC Schwetzingen, führte von Anfang bis Ende das Turnier an, und im Verfolgerfeld spielten die Boulefreunde aus Neckargerach und die Primeurs aus Walldorf um die Plätze 2 und 3.
            Alle Begegnungen wurden sehr fair und sportlich geführt und bereits gegen 17:45 Uhr konnte mit der Siegerehrung begonnen werden.</p>
